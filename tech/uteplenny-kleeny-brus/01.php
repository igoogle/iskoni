    <h2 class="section-title text-center">
        Что такое утепленный клееный брус?
    </h2>

    <p>
        Полый брус представляет собой деревянную конструкцию, очень похожую внешне на клееный брус, то же время напоминающую технологию «двойного бруса». Технология изготовления клееного полного бруса практически аналогична технологии изготовления клееных деревянных конструкций (сокращенно КДК) и представляет собой две сухие строганые доски (ламели) соединенные (склеенные) между собой соединительными элементами из склеенных ламелей длиной 12-15 см.
    </p>

    <p>
        Расстояние между элементами по длине бруса составляет 60 см. Вся эта система из досок и соединительных элементов «бобышек» подвергается фрезерованию на четырехстороннем станке с последующей нарезкой чашек под проект дома. В конструкции стены, формируемой деталями из полого бруса, соединительные элементы располагаются строго друг над другом, образуя вертикальные колонны в стене от фундамента до самой крыши.
    </p>

    <p>
        Очень важным является тот факт, что как и в профилированном брусе и в оцилиндрованном бревне, да и в клееном брусе, мы применяем практику монтажа полого бруса на шпильки диаметром 12-14 мм с шагом 2м на всю высоту стены. Это придает стене дополнительную конструктивную жесткость и прочность всей конструкции здания. Конструкция верхнего бревна в стене «мауэрлата» предполагает возможность подтягивания гаек на шпильках по мере усадки сруба. Образующиеся в конструкции стены вертикальные полости размером 60 на 14 см заполняются эффективным утеплителем - эковатой, что делает стену из клееного полого бруса очень теплой. Коэффициент теплового сопротивления стены R достигает 3,1. Для сравнения - R стандартного клееного бруса равно 1,2 при такой же толщине стены (22 см).
    </p>

